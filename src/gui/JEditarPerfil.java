    private void btnGuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuardarActionPerformed
        // TODO add your handling code here:
        usuario.setNombre(txtNombre.getText());
        usuario.setApellidos(txtApellidos.getText());
        usuario.setCelular(txtCelular.getText());
        usuario.setCorreo(txtCorreo.getText());
        usuario.setDni(txtDni.getText());
        usuario.setNacimiento((Date)spinNacimiento.getValue());
        if(rbtnMasculino.isSelected()){
            usuario.setGenero(1);
        }else{
            usuario.setGenero(2);
        }
        
        JOptionPane.showMessageDialog(null, "Valores cambiados");
        gui.getHeader().mostrarPanel(new JPerfil(gui).getContent());        
    }//GEN-LAST:event_btnGuardarActionPerformed

